const sectionCenter = document.querySelector(".section-center");
const container = document.querySelector(".btn-container");



// load items
window.addEventListener("DOMContentLoaded", function () {
  displayEarthItems(earth);
  displayEarthButtons();
});

function displayEarthItems(earthItems) {
  let displayEarth = earthItems.map(function (item) {
    // console.log(item);

    return `<article class="earth-item">
      <img src=${item.img} class="photo" alt=${item.title}>
      <div class="item-info">
        <header>
        <h4>${item.title}</h4>
        <h4 class="time">${item.time}</h4>
      </header>
      <p class="item-text">${item.desc}
      </p>
      </div>
    </article>`;
  });
  displayEarth = displayEarth.join("");
  console.log(displayEarth);

  sectionCenter.innerHTML = displayEarth;
};


function displayEarthButtons() {
  const places = earth.reduce(
    function (values, item) {
    if (!values.includes(item.place)) {
      values.push(item.place);
    }
  return values;
  },
  ["all"]
  );
  const placeBtns = places
  .map(function (place) {
    return `<button class="filter-btn" type="button"
    data-id=${place}>
      ${place}
    </button>`
  })
  .join("");
  container.innerHTML = placeBtns;
  const filterBtns = document.querySelectorAll(".filter-btn");
  // filter buttons
  filterBtns.forEach(function (btn) {
    btn.addEventListener("click", function (e) {
      const place = e.currentTarget.dataset.id;
      const earthPlace = earth.filter(function (earthItem) {
        // console.log(earthItem.place);
      if(earthItem.place === place) {
        return earthItem;
        }
      });
      // console.log(earthplace);
      if (place === "all") {
        displayEarthItems(earth);
      } else {
        displayEarthItems(earthPlace);
      }
    });
  });


}
